function setSelectedZoomItem(selectedItem) {
  if (selectedItem.inputId === "moedasZoom") {
    console.log("Zoom selecionado:", selectedItem);

    // Verifica se o campo Zoom que disparou a função é o com inputId = "moedasZoom"
    // (ou seja, o campo onde a pessoa escolheu a matrícula de alguém).

    document.getElementById("simbolo").value = selectedItem["simbolo"];
    document.getElementById("moeda").value = selectedItem["moeda"];
    document.getElementById("valor").value = selectedItem["valor"];
  }
  // preenchimento automatico dos campos inputs através de um dataset com informações correspondentes
}
